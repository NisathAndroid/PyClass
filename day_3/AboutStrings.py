# single quotes -> 'your content'
# double quotes -> " your content "
# Multiline quotes -> """ your content """ or ''' your content '''


your_content = 'my name is khan'

print(1,"->",your_content)

your_content = "my name is khan"

print(2,"->",your_content)

#Quotes Inside Quotes

your_content = ('my name is '
                'h'
                'h'
                'f'
                '"khan" ')

print(3,"->",your_content)

your_content = '''my
 name 
is "khan" 
'''

print(4,"->",your_content)
